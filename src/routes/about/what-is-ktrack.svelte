<h1>What is KTrack</h1>

<p>KTrack is a tool to help track your Vitamin K intake.</p>
<p>It provides a collection of data from public sources (e.g. the US Department of Argiculture) about the estimated amount of Vitamin K in various foods.
<p><em>If used for medical purposes, users must verify any estimates and treatments with their physician</em>.</p>
<p>Features include:</p>
<ul>
  <li>Record Daily intake.</li>
  <li>Store historical intake data.</li>
  <li>Search the data of foods.</li>
  <li>Add custom foods.</li>
</ul>

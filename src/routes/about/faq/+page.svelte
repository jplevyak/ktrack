<h3>What does "mgc/unit" mean?</h3>

The USDA differentiates betweeen true measures (i.e. a cup of water) and nominal measures (a cup of loose leaves). When the measure is not a true measure, the mgc amount is listed as "mcg/unit" while the description contains the measure (e.g. 1 cup).
